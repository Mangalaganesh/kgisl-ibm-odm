package main.tariffcalculation;

public class Discount {

	private double fleetPercent;

	private double ncbPercent;

	private double directPercent;

	private double redPlatePercent;

	private double cctvPercent;

	public double getFleetPercent() {
		return fleetPercent;
	}

	public void setFleetPercent(double fleetPercent) {
		this.fleetPercent = fleetPercent;
	}

	public double getNcbPercent() {
		return ncbPercent;
	}

	public void setNcbPercent(double ncbPercent) {
		this.ncbPercent = ncbPercent;
	}

	public double getDirectPercent() {
		return directPercent;
	}

	public void setDirectPercent(double directPercent) {
		this.directPercent = directPercent;
	}

	public double getRedPlatePercent() {
		return redPlatePercent;
	}

	public void setRedPlatePercent(double redPlatePercent) {
		this.redPlatePercent = redPlatePercent;
	}

	public double getCctvPercent() {
		return cctvPercent;
	}

	public void setCctvPercent(double cctvPercent) {
		this.cctvPercent = cctvPercent;
	}

}
